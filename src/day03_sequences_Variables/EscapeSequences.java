package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \nMy name is Cybertek"); // one single line
                                            //after \n +new line
                                           // paragraph space tab bottom
        System.out.println("\tI am in Virginia\n"); // tab bottom
        System.out.println("\n\n\t\t\tI love programing");


        System.out.println("\\");  // only prints me  one back slash
        System.out.println("/");   // this prints me one front slash
        System.out.println("My name is \'Muhtar\'"); // 'Muhtar' single quote
        System.out.println("My name is 'Muhtar'");// 'Muhtar'

        System.out.println("my favorite TV series: \"game of thrones\" "); //only way for double code





    }
}
