package day21_Loops;

public class DoWhile {
    public static void main(String[] args) {

// sadece condition is false ise arasindaki fark do - while loop get executed print statement
        boolean result = false;

        while(result){
            System.out.println("Hello World");
        }

        System.out.println("========================");

        do{ // just do it
            System.out.println("Hello World");
        }while(result); // true olursa infinitive times gets execute






    }

}
